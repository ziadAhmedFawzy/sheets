num_of_week = int(input("please enter number of week : "))

if num_of_week == 1:
    print("saturday")
elif num_of_week == 2:
    print("sunday")
elif num_of_week == 3:
    print("monday")
elif num_of_week == 4:
    print("tuesday")
elif num_of_week == 5:
    print("wednesday")
elif num_of_week == 6:
    print("thursday")
elif num_of_week == 7:
    print("friday")
else:
    print("enter number from 1 to 7 ")
